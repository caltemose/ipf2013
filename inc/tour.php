		        <h3>768 Lake Avenue</h3>
<p>
The unusual and unique small building at 768 Lake Avenue is often mistaken for a commercial property.&nbsp; Although it is now the private residence of Amy and Trey Palmer, it was indeed used as a store for approximately 90 years.
</p>

<p>
The &ldquo;flatiron&rdquo; upper half of the house was built as a corner store in 1907 by prominent Atlanta real estate developer Fitzhugh Knox.&nbsp; The location was chosen because there was a trolley line running from Lake Avenue onto West Ashland at this corner.&nbsp; The store was a triangular wood-frame building with a tar-and-gravel roof; the original siding can be seen on the long downstairs wall, and the exposed wood ceiling is the underside of the original roof deck.
</p>

<p>
The store was bricked on two sides and a shed-roof added on, probably in the 1940s or 1950s.&nbsp; Longtime Inman Park residents recall the plumbing store that occupied the property since at least the 1970s; the present patio, now an attractive sitting area, was an open lot filled with old appliances and toilets.
</p>

<p>
Michael Bond bought the store for his heating and air conditioning business in 1996, and subsequently renovated it as a loft-style private residence with a substantial front patio now occupying the triangular corner.
</p>

<p>
Now Trey, Amy and bulldogs Fat Boy and Petunia call 768 Lake Avenue home, and enjoy living in one of the most unusual dwellings in Inman Park.
</p>

