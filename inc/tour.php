		        <h3>484 Sinclair Avenue</h3>
<p>
Having just celebrated its 100 year anniversary in 2012, the home at 484 Sinclair Avenue was originally built as an up/down duplex. In 1996, new Inman Park residents Regina Brewer and Joe Castellano bought this rental with eyes on renovation. Their home was last on tour in 1999. Now, 17 years and three children later, it is a fully renovated single family home with a cozy basement apartment where Regina&rsquo;s father, Bill Brewer, lives with his dog Willie. Saving as much of the original historic fabric of the house was paramount for Regina who is a professional preservationist. The wood windows are all original (with much of the original glass) and have been restored by Joe. Original wood floors, plaster walls, trim, doors, and hardware have been lovingly restored as well. The kitchen and screened porch (designed by friend and neighbor Tamara Jones) were added in 2009, making the space the favorite gathering location for everyone who visits. In 2011, the front and rear gardens were completely redone to allow for a maintenance-free yard that is both beautiful and kid-friendly.
</p>

<p>
The d&eacute;cor of the house reflects Joe and Regina&rsquo;s love of antiques and contemporary art. Their favorite painting, &ldquo;Wilbur,&rdquo; located in the entry hall, was painted by friend and neighbor Scott Eakin. The guest bedroom downstairs is furnished with a bedroom suite from Regina&rsquo;s great aunt and the bedspread was handmade by her grandmother for her wedding in 1917. Pictures of both Joe and Regina&rsquo;s relatives are located throughout the house as a reminder of their deep love of family.
</p>

<p>
The Castellano/Brewer family loves being a part of the Inman Park community and they hope you enjoy your visit to their home!
</p>
