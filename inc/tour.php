		        <h3>413 Sinclair Avenue</h3>
<p>
When Leigh Hays, a real estate agent, was previewing property in the neighborhood in early 1998, she was definitely not in the market for a house for herself. But when she walked into the living room of this 1939 Craftsman bungalow, it was truly love at first sight. In fact, she bought the house before seeing all of it, something she&rsquo;d never let her clients do! As Leigh tells it, &ldquo;I was so excited that I stopped opening doors once in the master bedroom and missed the master bath!&rdquo;</p>
<p>
That love at first sight turned into a match made in heaven, because 413 Sinclair Avenue was a house that certainly needed lots of love. A rental property for over 12 years, the house was run-down and in need of TLC and all new systems, but it had &ldquo;good bones&rdquo; and wonderful details: living room bookcases with arched tops, perfectly delightful dining room cubby that has now been turned into a smart liquor cabinet, stained glass window in the sitting room, cast iron tub in the bathroom, four fireplaces, and exquisite antique light fixtures. What&rsquo;s not to really love now?!</p>
<p>
Prior to Leigh&rsquo;s move-in later in the year, major renovations took place throughout the house, especially in the kitchen. Along with that undertaking, Leigh also tackled the hall bathroom and, a year later, the master bedroom. In 2006, her friend and designer/artist, Rory Powers, worked with her on the d&eacute;cor of most of the house. These loving touches can been seen in the living room, dining room, master suite and the smaller guest bedroom. Several of Rory&rsquo;s paintings have found special places throughout the house.</p>
<p>
Leigh loves her home inside and out and laughingly says, &ldquo;One of the best spots in the house is actually the front porch.&rdquo;
</p>

