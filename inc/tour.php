		        <h3>1107 Austin Avenue</h3>
<p>
Carol and Ben Mitchell don&rsquo;t like to give up on a good thing.&nbsp; They lived in Inman Park in the 1990s in their home on Elizabeth Street.&nbsp; They loved their neighborhood and tirelessly volunteered for all things Inman Park (especially Festival!) until a new job took them to New York and a loft in Greenwich Village.&nbsp; They lived there for 20 years, but within a year of Ben&rsquo;s retirement, they were back in their favorite neighborhood.
</p>

<p>
1107 Austin Avenue is their second home in Inman Park and has wonderfully settled feel to it.&nbsp; You&rsquo;ll see that the big, open Craftsman-style rooms are a perfect showcase for their much-loved art collection, and there&rsquo;s a definite loft-like vibe in their decorating style.
</p>

<p>
Among the highlights in this beautiful home are a cloth sculpture of an Egyptian Princess racing an ostrich created by Lisa Lichtenfels; a painting by Bruno Zupan; and a Carol-created crazy quilt in the living room.&nbsp; The dining room features pictures of some of their previous residences (including their home on Elizabeth Street,) and Ben&rsquo;s photography is located above the stairs.&nbsp; Prints by G.H. Rothe can be found in the sunny master bedroom. The wire dancers in the sitting room are reminders of the Mitchell kids&rsquo; professional ballet dancing days.&nbsp; Be sure to also note the leaded, beveled glass in the sitting room, which reflects rainbows around the room on sunny days.&nbsp; Their kitchen renovation combines modern appliances and cabinetry with the original vintage butler&rsquo;s pantry and quaint built-in ironing board.
</p>

<p>
Carol&rsquo;s studio on the first floor is a riot of color and creativity and perfect for making dolls, doll clothes, quilts and various other Carol creations.&nbsp; You&rsquo;ll also enjoy seeing Ben&rsquo;s photography studio which is located upstairs along with the library and guest room.
</p>
