		        <h3>970 Waverly Way</h3>
<p>
Built for $2,500 in 1915 by owner-architect Richard Wye, this Craftsman-style bungalow underwent a major addition and renovation in 2003 when Aimee and Nick bought it.&nbsp; They renovated the bathrooms and the kitchen (of course!) and added 400 square feet of family and dining space at the back of the home.&nbsp; They&rsquo;ve brought in their sense of color, light and artistry throughout.&nbsp; Now fast-forward to early 2013 when the homeowners redesigned the upstairs to include a master suite, added a wine cellar and installed a shower in the downstairs guest bathroom&hellip;with the goal of completion in time for the 2013 festival.&nbsp; They made it!
</p>

<p>
The Franzes &ldquo;weren&rsquo;t even talking about moving&rdquo; when they looked at the house for the first time and fell in love with those amazing old house features: bubble glass, pocket doors with window panes, beautiful solid wood construction and high, high ceilings.&nbsp; Maybe having Freedom Park one house down and the easy walking distance to one of their favorite restaurants were signs that this was to be their home.&nbsp; They love the fact that they know so many of their neighbors and have stayed involved in the neighborhood, enjoying its strong sense of community.
</p>

<p>
Original features in the home include a butler&rsquo;s panty, sleeping porch (now enclosed) and transoms.&nbsp; The farmhouse sink and suspended glass cabinets give a county feel to the back area while the front remains a bit more formal.&nbsp; One-of-a-kind family antiques are lovingly placed throughout the house, including the empire-style dining room furniture and various unique wicker pieces.&nbsp; The large square table in the family dining area was purchased at a previous year&rsquo;s Inman Park Festival.&nbsp; The tables in the hall rest on ornamental grid work from a bridge in Pennsylvania and note the stunning Turkish rug in the living room which was acquired on a recent visit to Istanbul.&nbsp; The Franzes may love to travel but they love to come home to Inman Park even more.
</p>

