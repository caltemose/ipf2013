		        <h3>The Wrecking Bar Brewery<br/>292 Moreland Ave. NE</h3>
<p>
Frequently referred to as &ldquo;The Wrecking Bar,&rdquo; this remarkable 1900 Victorian house has eclectic elements of Roman, Renaissance and Baroque architecture and is highly recognizable with its beautiful semicircular front portico and classical Corinthian columns. The architect was Willis F. Denny II, who also designed Rhodes Hall and many other notable buildings in his short career. Originally, the building was the residence of Victor H. Kriegshaber who served in executive positions for countless business, civic, philanthropic and cultural organizations. Additionally, this building has been a church, a dance studio, and an architectural antiques operation which is where the Wrecking Bar name originates.
</p>

<p>
In 2009, Bob and Kristine Sandage purchased The Wrecking Bar, and have turned it in to the successful business it is today. The renovations include the restoration of the main level to its original glory and its transformation into an event facility called The Marianna. The granite foundation in the basement is the perfect place for The Wrecking Bar Brewpub, with the brewery in the warehouse behind the main building. Bob has personally spent many hours involved in the project: cleaning graffiti, painting, jack hammering concrete in the basement, and many other fun tasks. But Bob and Kristine say it&rsquo;s all been a labor of love. They were also smart enough to hold numerous volunteer days where friends and neighbors helped dig in and restore the building.
</p>

<p>
