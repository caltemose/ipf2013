		        <h3>946 Waverly Way</h3>
<p>
Sometimes, to get what you want, you have to keep trying.
</p>

<p>
That&rsquo;s what Jeanne LaSala and her husband Jim Abbot will tell you. In 1984, they were newlyweds renting an apartment nearby. They fell in love with the older homes and tree-lined streets of Inman Park. Life got in the way, though. It took sixteen years for them to get back, accompanied by sons Thomas and Andrew.
</p>

<p>
Even then, Jeanne and Jim had to &ldquo;settle&rdquo; for new construction. Although everyone reassured them that their Craftsman-style home was wonderfully compatible in scale and design with the rest of the neighborhood (visitors sometimes asked, &ldquo;When did you renovate?&rdquo;) they weren&rsquo;t entirely satisfied.
</p>

<p>
So try and try again. In the foyer, for example, they&rsquo;ve replaced the original staircase with a new one, entirely custom, and added a display cabinet featuring art glass with a dragon-fly motif. In the octagonal parlor, they scaled down the fireplace mantel, and new pocket doors with stained glass lights separate the parlor from the dining room. They started over in their master bath, adding a wainscot of impressed art tile for a contemporary take on a centuries-old technique. Downstairs, the basement is now finished with a study, full bath, and exercise area. Last but not least, they have furnished their home in a way that speaks to their enthusiasms: look for signs everywhere of Jeanne&rsquo;s interest in the Arts &amp; Crafts era and, in their artwork, of Jim&rsquo;s passion for trees.
Almost 30 years after they first laid eyes on Inman Park, Jeanne and Jim now call it home. Mission accomplished.
</p>
